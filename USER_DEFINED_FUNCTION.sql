DROP FUNCTION IF EXISTS FULLNAME;

DELIMITER //

CREATE FUNCTION FULLNAME(
    FIRST_NAME VARCHAR(50),
    LAST_NAME VARCHAR(50)
)
RETURNS VARCHAR(101)
DETERMINISTIC
BEGIN
    RETURN CONCAT(FIRST_NAME, ' ', LAST_NAME);
END //

DELIMITER ;
SELECT FULLNAME('AISHU','POTABATTI');

-- 3. UDF TO CHECK EVEN NUMBER
DELIMITER //

CREATE FUNCTION CHECK_EVEN(A INT)
RETURNS BOOLEAN
DETERMINISTIC
BEGIN
    IF A%2=0 THEN 
    RETURN TRUE;
    ELSE
		RETURN FALSE;
	END IF;
    
END //

DELIMITER ;
SELECT CHECK_EVEN(3534);

-- 4. UDF TO GET AGE FROM BIRTHDATE
DELIMITER //
CREATE FUNCTION GET_BIRTHDATE(DOB DATE)
RETURNS INT
DETERMINISTIC
BEGIN
	RETURN TIMESTAMPDIFF(YEAR,DOB,CURDATE());
END //
DELIMITER ;
SELECT GET_BIRTHDATE('2004-09-24')

-- 5. UDF TO CALCULATE SIMPLE INTEREST

DELIMITER //

CREATE FUNCTION SIMPLE_INTEREST(
    P DECIMAL(10,2),
    R DECIMAL(5,2),
    T DECIMAL(5,2)
)
RETURNS DECIMAL(10,2)
DETERMINISTIC
BEGIN
    RETURN (P * R * T) / 100;
END //

DELIMITER ;
SELECT SIMPLE_INTEREST(1000, 5, 2);

-- 6. UDF TO CHECK IF STRING IS PALINDROME
DROP FUNCTION IF EXISTS PALINDROME;
DELIMITER //

CREATE FUNCTION PALINDROME(S VARCHAR(100))
RETURNS BOOLEAN
DETERMINISTIC
BEGIN
    IF S = REVERSE(S) THEN
        RETURN TRUE;
    ELSE
        RETURN FALSE;
    END IF;
END //

DELIMITER ;
SHOW CREATE FUNCTION PALINDROME;
SELECT PALINDROME('HELLO');

