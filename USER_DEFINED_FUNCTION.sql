-- 8. UDF TO GET FIRST LETTER OF NAME CAPITALIZED
DELIMITER //

CREATE FUNCTION GET_CAP(NAME VARCHAR(100))
RETURNS VARCHAR(100)
DETERMINISTIC
BEGIN
    RETURN CONCAT(
        UPPER(LEFT(NAME,1)),
        LOWER(SUBSTRING(NAME,2))
    );
END //

DELIMITER ;
SELECT GET_CAP("word");

-- 9. UDF TO CALCULATE FACTORIAL
DROP FUNCTION IF EXISTS FACTORIAL;
DELIMITER //
CREATE FUNCTION FACTORIAL(N INT)
RETURNS BIGINT
DETERMINISTIC
BEGIN
	DECLARE F BIGINT DEFAULT 1;
    WHILE N>0 DO
		SET F=F*N;
        SET N=N-1;
	END WHILE;
    RETURN F;
END //
DELIMITER ;
SELECT FACTORIAL(5);

-- 10. UDF TO MASK A PHONE NUMBER(E.G., 1234567890-->****7890)
DELIMITER //
CREATE FUNCTION M_PHONE(PHONE VARCHAR(20))
RETURNS VARCHAR(100)
DETERMINISTIC
BEGIN
	RETURN CONCAT(REPEAT('*',LENGTH(PHONE)-4),RIGHT(PHONE,4));
END //
DELIMITER ;
SELECT M_PHONE('1234567890');

-- 11. UDF TO GET DISCOUNT ON PRICE
DELIMITER //
CREATE FUNCTION GET_DISCOUNT(PRICE DECIMAL(10,2), DISCOUNT_PERCENT INT)
RETURNS DECIMAL(10,2)
DETERMINISTIC
BEGIN 
	DECLARE DISCOUNT_PRICE DECIMAL(10,2);
    SET DISCOUNT_PRICE=PRICE-(PRICE*DISCOUNT_PERCENT/100);
    RETURN DISCOUNT_PRICE;
END //
DELIMITER ;
SELECT GET_DISCOUNT(10000,80);







