USE APRILPYTHON;
-- FUNCTION
-- PREDEFINE FUNCTION
-- 3.DATE AND TIME FUNCTION
-- 3.1 NOW():
SELECT NOW();
-- 3.2 CURDATE():
SELECT CURDATE();
-- 3.3 CURTIME():
SELECT CURTIME();
-- 3.4 DATE():
SELECT DATE('2025-06-20,10:00:00');
-- 3.5 TIME():
SELECT TIME('2026-10-28, 12:25:30');
-- 3.6 DATE_ADD():
SELECT DATE_ADD('2025-10-26',INTERVAL 5 DAY);
-- 3.7 DATEDIFF():
SELECT DATEDIFF('2026-01-28','2026-01-15');
-- 3.8 TIMESTAMP():
SELECT TIMESTAMP('2026-01-25',12-12-30);
-- 3.9 YEAR():
SELECT YEAR('2025-10-23');
-- 3.10 HOUR():
SELECT HOUR('12:00:00');
